bool move() {
    short row;
    short column;
    for (int i = 0; i < 851; i++) {
       switch(maze[i]) {
            case 0:
               std::cout << ' ';
               break;
            case 1:
                std::cout << '+';
                break;
            default:
                std::cout << '@';
                row = i / 37;
                column = i % 37;
        }
        if (i % 37 == 36) {
            std::cout << '\n';
        }
    }
    char move;
    std::cin >> move;
    switch(move) {
        case 'w':
            if (maze[(row - 1) * 37 + column] == 1) {
                std::cout << "Invalid Move\n";
                return false;
            }
            maze[(row - 1) * 37 + column] = 2;
            break;
        case 's':
            if (maze[(row + 1) * 37 + column] == 1) {
                std::cout << "Invalid Move\n";
                return false;
            }
            maze[(row + 1) * 37 + column] = 2;
            break;
        case 'd':
            if (maze[row * 37 + (column + 1)] == 1) {
                std::cout << "Invalid Move\n";
                return false;
            }
            maze[row * 37 + (column + 1)] = 2;
            break;
        case 'a':
            if (maze[row * 37 + (column - 1)] == 1) {
                std::cout << "Invalid Move\n";
                return false;
            }
            maze[row * 37 + (column - 1)] = 2;
            break;
        default:
            std::cout << "Invalid Input\n";
    }
    maze[row * 37 + column] = 0;
    return true;
};

int main() {
    while (true) {
        move();
    }
    return 0;
}
